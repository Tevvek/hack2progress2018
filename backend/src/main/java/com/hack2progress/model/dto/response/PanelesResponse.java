package com.hack2progress.model.dto.response;

public class PanelesResponse {
	private Integer numPaneles;
	private Integer numBaterias;
	private Integer potenciaInversor;
	public Integer getNumPaneles() {
		return numPaneles;
	}
	public void setNumPaneles(Integer numPaneles) {
		this.numPaneles = numPaneles;
	}
	public Integer getNumBaterias() {
		return numBaterias;
	}
	public void setNumBaterias(Integer numBaterias) {
		this.numBaterias = numBaterias;
	}
	public Integer getPotenciaInversor() {
		return potenciaInversor;
	}
	public void setPotenciaInversor(Integer potenciaInversor) {
		this.potenciaInversor = potenciaInversor;
	}

}
